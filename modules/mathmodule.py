import math as m
# pi value
print(m.pi)

# e
print(m.e)

# power
print(m.pow(5,2))

# factorial
print(m.factorial(5))

# product 

print(m.prod([1,2,3,4,5,6]))

# floor / round to the lowest value 
print(m.floor(2.6))
# ceil
print(m.ceil(4.1))

# trunc / removes the decimal value 
print(m.trunc(2.6))